namespace Screenbox.Controls;

/// <summary>
/// Defines constants that specify the height of the <see cref="TitleBar"/>.
/// </summary>
public enum TitleBarHeightMode
{
    /// <summary>
    /// The title bar uses the standard height of 32 device-independent pixels (DIPs).
    /// </summary>
    Standard,

    /// <summary>
    /// The title bar is set to a height of 48 device-independent pixels (DIPs)
    /// to better accommodate interactive elements.
    /// </summary>
    Tall,
}
